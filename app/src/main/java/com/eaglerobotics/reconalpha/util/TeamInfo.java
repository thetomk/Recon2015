package com.eaglerobotics.reconalpha.util;

public class TeamInfo {
	private String teamNum;
	private int goodPct;
	private String allComments;
	private String synopsis;
	private String alliance;
	private int commentRank;
	private int rating;
	private String ratingLabel;


	
	
	public String getTeamNum() {
		return teamNum;
	}

	public void setTeamNum(String teamNum) {
		this.teamNum = teamNum;
	}

	public int getGoodPct() {
		return goodPct;
	}

	public void setGoodPct(int goodPct) {
		this.goodPct = goodPct;
	}
	public String getAllComments() {
		return allComments;
	}

	public void setAllComments(String allComments) {
		this.allComments = allComments;
	}

	public String getSynopsis() {
		return synopsis;
	}

	public void setSynopsis(String synopsis) {
		this.synopsis = synopsis;
	}

	public String getAlliance() {
		return alliance;
	}

	public void setAlliance(String alliance) {
		this.alliance = alliance;
	}

	public int getCommentRank() {
		return commentRank;
	}

	public void setCommentRank(int commentRank) {
		this.commentRank = commentRank;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

	public String getRatingLabel() {
		return ratingLabel;
	}

	public void setRatingLabel(String ratingLabel) {
		this.ratingLabel = ratingLabel;
	}

}

